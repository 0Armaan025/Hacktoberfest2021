# Python program to swap values two variables

x = int(input("Enter value of x:" ))# To take inputs from the user
y = int(input("Enter value if y:"))# To take inputs from the user




# create a temporary variable and swap the values
temp = x
x = y
y = temp

print('The value of x after swapping: {}'.format(x))
print('The value of y after swapping: {}'.format(y))
